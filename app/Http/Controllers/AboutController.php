     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\About  $about
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $about = About::find($id);

        $request->validate([
            'description_history' => 'required',
            'description_vision' => 'required',
            'description_mission1' => 'required',
            'description_mission2' => 'required',
            'description_mission3' => 'required',
            'description_ceo' => 'required',
            'description_secretary' => 'required',
            'description_treasurer' => 'required',
            'description_supervisor' => 'required',
            'description_member1' => 'required',
            'description_member2' => 'required',
        ]);

        $input = $request->all();

        $about->update($input);

        return redirect('/admin/about')->with('message', 'Data Berhasil Diedit');
    }

